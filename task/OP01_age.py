# Написать программу, которая будет запрашивать у пользователя его имя и возраст, а затем выводить приветствие “Привет [имя]! Тебе [возраст]”.
# Посчитай также количество прожитых месяцев, дней, часов.


# Запрос имени и возраста
name = input("Введите ваше имя: ")
age = int(input("Введите ваш возраст (в годах): "))

# Вывод приветствия
print(f"\nПривет, {name}! Тебе {age} года.")

# Расчёт прожитых единиц времени
months = age * 12
days = age * 365
hours = days * 24

# Вывод результатов
print("Ты прожил примерно:")
print(f"- {months} месяцев")
print(f"- {days} дней")
print(f"- {hours} часов")