import math

print(math.ceil(3.14))
# ceil - округление в большую сторону
print("\n")
print(math.floor(3.14))
# floor - округление в меньшую сторону
print("\n")
print(math.log(100, 10))
# log - логарифм 100 по основанию 10
print("\n")
print(math.sqrt(16))
print("\n")
print(math.pow(9, 2))
print("\n")
print(math.pi)
print("\n")